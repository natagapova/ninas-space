function initRunnerGame() {
  const startBtn = document.getElementById("runnerStart");
  const jumpBtn = document.getElementById("runnerJump");
  const kitty = document.getElementById("runnerKitty");
  const obstacle = document.getElementById("runnerObstacle");
  const scoreEl = document.getElementById("runnerScore");
  const statusEl = document.getElementById("runnerStatus");
  if (!startBtn || !jumpBtn || !kitty || !obstacle || !scoreEl || !statusEl) return;

  let running = false;
  let obstacleX = 280;
  let score = 0;
  let obstaclePassed = false;
  let raf = null;
  let y = 8;
  let vy = 0;
  const gravity = 0.92;
  const jumpPower = 10.8;
  const ground = 8;

  function endRun() {
    running = false;
    if (raf) cancelAnimationFrame(raf);
    const dict = translations[getLang()] || translations.en;
    statusEl.textContent = dict.game7StatusOver;
  }

  function jump() {
    if (!running) return;
    if (y <= ground + 0.2) {
      vy = jumpPower;
    }
  }

  function frame() {
    if (!running) return;
    vy -= gravity;
    y += vy;
    if (y < ground) {
      y = ground;
      vy = 0;
    }
    kitty.style.bottom = `${y}px`;

    obstacleX -= 4.1;
    if (obstacleX < -38) {
      obstacleX = 280;
      obstaclePassed = false;
    }
    obstacle.style.left = `${obstacleX}px`;

    if (!obstaclePassed && obstacleX + 34 < 18) {
      obstaclePassed = true;
      score += 1;
      scoreEl.textContent = score.toString();
    }

    const kittyLeft = 16;
    const kittyRight = kittyLeft + 32;
    const kittyBottom = y;
    const kittyTop = y + 32;
    const obsLeft = obstacleX;
    const obsRight = obstacleX + 30;
    const obsBottom = 8;
    const obsTop = 8 + 28;
    const overlap =
      kittyLeft < obsRight &&
      kittyRight > obsLeft &&
      kittyBottom < obsTop &&
      kittyTop > obsBottom;
    if (overlap) {
      endRun();
      return;
    }

    raf = requestAnimationFrame(frame);
  }

  startBtn.addEventListener("click", () => {
    if (running) return;
    running = true;
    score = 0;
    obstacleX = 280;
    obstaclePassed = false;
    y = ground;
    vy = 0;
    kitty.style.bottom = `${ground}px`;
    obstacle.style.left = `${obstacleX}px`;
    scoreEl.textContent = "0";
    statusEl.textContent = "";
    if (raf) cancelAnimationFrame(raf);
    raf = requestAnimationFrame(frame);
  });

  jumpBtn.addEventListener("click", jump);
}

function initReflexGame() {
  const startBtn = document.getElementById("reflexStart");
  const tapBtn = document.getElementById("reflexTap");
  const status = document.getElementById("reflexStatus");
  const best = document.getElementById("reflexBest");
  if (!startBtn || !tapBtn || !status || !best) return;
  const icon = document.getElementById("reflexIcon");
  if (!icon) return;

  let waiting = false;
  let active = false;
  let startMs = 0;
  let bestMs = null;
  let waitTimer = null;
  let targetIsKitty = false;

  const iconPool = [
    "assets/pixel-kitty-runner.svg",
    "assets/pixel-bow.svg",
    "assets/pixel-flower.svg",
    "assets/pixel-heart.svg"
  ];

  startBtn.addEventListener("click", () => {
    const dict = translations[getLang()] || translations.en;
    waiting = true;
    active = false;
    targetIsKitty = false;
    tapBtn.disabled = false;
    icon.src = "assets/pixel-heart.svg";
    status.textContent = dict.game9Ready;
    if (waitTimer) clearTimeout(waitTimer);
    const delay = 1000 + Math.floor(Math.random() * 2000);
    waitTimer = setTimeout(() => {
      waiting = false;
      active = true;
      startMs = Date.now();
      status.textContent = dict.game9Now;
      const choice = iconPool[Math.floor(Math.random() * iconPool.length)];
      icon.src = choice;
      targetIsKitty = choice.includes("kitty");
    }, delay);
  });

  tapBtn.addEventListener("click", () => {
    const dict = translations[getLang()] || translations.en;
    if (waiting) {
      waiting = false;
      active = false;
      if (waitTimer) clearTimeout(waitTimer);
      status.textContent = dict.game9TooSoon;
      tapBtn.disabled = true;
      return;
    }
    if (!active) return;
    active = false;
    if (!targetIsKitty) {
      status.textContent = dict.game9TooSoon;
      tapBtn.disabled = true;
      return;
    }
    const ms = Date.now() - startMs;
    status.textContent = `${ms} ms`;
    if (bestMs === null || ms < bestMs) {
      bestMs = ms;
      best.textContent = bestMs.toString();
    }
    tapBtn.disabled = true;
  });
}

