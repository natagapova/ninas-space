function applyLanguage(lang) {
  const dict = translations[lang] || translations.en;
  document.documentElement.lang = lang === "sr" ? "sr" : "en";

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (dict[key]) el.textContent = dict[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.dataset.i18nPlaceholder;
    if (dict[key]) el.setAttribute("placeholder", dict[key]);
  });

  updateAudioButtonLabel(dict);
  updateBreathingButtonLabel(dict);
}

function ensureAmbientContext() {
  if (!ambientCtx) {
    ambientCtx = new (window.AudioContext || window.webkitAudioContext)();
  }
  return ambientCtx;
}

function createNoiseBuffer(ctx, tint = "brown") {
  const length = ctx.sampleRate * 2;
  const buffer = ctx.createBuffer(1, length, ctx.sampleRate);
  const output = buffer.getChannelData(0);
  let prev = 0;
  for (let i = 0; i < length; i += 1) {
    const white = Math.random() * 2 - 1;
    if (tint === "white") output[i] = white * 0.7;
    else if (tint === "pink") {
      prev = 0.98 * prev + 0.02 * white;
      output[i] = prev * 2.2;
    } else {
      prev = (prev + 0.02 * white) / 1.02;
      output[i] = prev * 3.5;
    }
  }
  return buffer;
}

function fadeInGain(ctx, gainNode, target = 0.03, seconds = 1.3) {
  const now = ctx.currentTime;
  gainNode.gain.cancelScheduledValues(now);
  gainNode.gain.setValueAtTime(0.0001, now);
  gainNode.gain.linearRampToValueAtTime(target, now + seconds);
}

function fadeOutAndCleanup(bundle, seconds = 0.8) {
  if (!bundle || !bundle.ctx || !bundle.master || !bundle.cleanup) return;
  const now = bundle.ctx.currentTime;
  bundle.master.gain.cancelScheduledValues(now);
  bundle.master.gain.setValueAtTime(bundle.master.gain.value, now);
  bundle.master.gain.linearRampToValueAtTime(0.0001, now + seconds);
  setTimeout(() => {
    bundle.cleanup();
  }, (seconds + 0.05) * 1000);
}

function createNoiseMode(ctx, config) {
  const source = ctx.createBufferSource();
  source.buffer = createNoiseBuffer(ctx, config.tint || "brown");
  source.loop = true;

  const filter = ctx.createBiquadFilter();
  filter.type = config.filterType || "lowpass";
  filter.frequency.value = config.freq || 500;
  filter.Q.value = config.q || 0.8;

  const master = ctx.createGain();
  master.gain.value = 0.0001;

  source.connect(filter);
  filter.connect(master);
  master.connect(ctx.destination);

  source.start();

  const modOsc = config.mod
    ? (() => {
        const osc = ctx.createOscillator();
        const modGain = ctx.createGain();
        osc.type = "sine";
        osc.frequency.value = config.mod.rate;
        modGain.gain.value = config.mod.depth;
        osc.connect(modGain);
        modGain.connect(filter.frequency);
        osc.start();
        return { osc, modGain };
      })()
    : null;

  fadeInGain(ctx, master, config.volume || 0.03, 1.4);

  return {
    ctx,
    master,
    cleanup: () => {
      source.stop();
      source.disconnect();
      filter.disconnect();
      master.disconnect();
      if (modOsc) {
        modOsc.osc.stop();
        modOsc.osc.disconnect();
        modOsc.modGain.disconnect();
      }
    }
  };
}

function createLofiMode(ctx) {
  const master = ctx.createGain();
  master.gain.value = 0.0001;
  master.connect(ctx.destination);

  const bass = ctx.createOscillator();
  const bassGain = ctx.createGain();
  bass.type = "triangle";
  bass.frequency.value = 98;
  bassGain.gain.value = 0.018;
  bass.connect(bassGain);
  bassGain.connect(master);
  bass.start();

  const keys = ctx.createOscillator();
  const keysGain = ctx.createGain();
  keys.type = "sine";
  keysGain.gain.value = 0.012;
  keys.connect(keysGain);
  keysGain.connect(master);
  keys.start();

  const notes = [261.63, 293.66, 329.63, 293.66];
  let i = 0;
  const seq = setInterval(() => {
    keys.frequency.setTargetAtTime(notes[i % notes.length], ctx.currentTime, 0.08);
    i += 1;
  }, 700);

  fadeInGain(ctx, master, 0.03, 1.2);

  return {
    ctx,
    master,
    cleanup: () => {
      clearInterval(seq);
      bass.stop();
      keys.stop();
      bass.disconnect();
      bassGain.disconnect();
      keys.disconnect();
      keysGain.disconnect();
      master.disconnect();
    }
  };
}

function createModeBundle(ctx, mode) {
  if (mode === "rain") {
    return createNoiseMode(ctx, { tint: "white", filterType: "highpass", freq: 1300, q: 0.2, volume: 0.02 });
  }
  if (mode === "fire") {
    return createNoiseMode(ctx, { tint: "pink", filterType: "bandpass", freq: 420, q: 0.9, volume: 0.022, mod: { rate: 2.4, depth: 90 } });
  }
  if (mode === "wind") {
    return createNoiseMode(ctx, { tint: "white", filterType: "bandpass", freq: 260, q: 1.1, volume: 0.02, mod: { rate: 0.18, depth: 140 } });
  }
  if (mode === "trees") {
    return createNoiseMode(ctx, { tint: "pink", filterType: "bandpass", freq: 620, q: 0.6, volume: 0.018, mod: { rate: 0.11, depth: 120 } });
  }
  if (mode === "lofi") {
    return createLofiMode(ctx);
  }
  return createNoiseMode(ctx, { tint: "brown", filterType: "lowpass", freq: 360, q: 0.8, volume: 0.028 });
}

function startAmbientAudio() {
  const ctx = ensureAmbientContext();
  if (ambientNodes) return;
  ambientNodes = createModeBundle(ctx, currentSoundMode);
}

function stopAmbientAudio() {
  if (!ambientNodes) return;
  const old = ambientNodes;
  ambientNodes = null;
  fadeOutAndCleanup(old, 0.9);
}

function restartAmbientAudioIfOn() {
  if (!ambientOn) return;
  stopAmbientAudio();
  startAmbientAudio();
}

function updateAudioButtonLabel(dict) {
  const audioBtn = document.getElementById("audioToggle");
  if (!audioBtn) return;
  audioBtn.textContent = ambientOn ? dict.audioToggleOn : dict.audioToggleOff;
}

function initAmbientToggle() {
  const audioBtn = document.getElementById("audioToggle");
  const soundSelect = document.getElementById("soundSelect");
  if (!audioBtn) return;

  ambientOn = getAmbientState();
  currentSoundMode = getSoundMode();
  if (soundSelect) {
    soundSelect.value = currentSoundMode;
    soundSelect.addEventListener("change", () => {
      currentSoundMode = soundSelect.value;
      setSoundMode(currentSoundMode);
      restartAmbientAudioIfOn();
    });
  }

  if (ambientOn) {
    startAmbientAudio();
  }

  audioBtn.addEventListener("click", async () => {
    try {
      const ctx = ensureAmbientContext();
      if (ctx.state === "suspended") {
        await ctx.resume();
      }
      ambientOn = !ambientOn;
      setAmbientState(ambientOn);
      if (ambientOn) startAmbientAudio();
      else stopAmbientAudio();
      const lang = getLang();
      updateAudioButtonLabel(translations[lang] || translations.en);
    } catch (_) {
      ambientOn = false;
      setAmbientState(false);
    }
  });
}

