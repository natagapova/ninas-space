let breathTimer = null;
let breathRunning = false;
let breathPhaseIndex = 0;
let breathTick = 0;

const breathPhases = [
  { key: "breathIn", seconds: 5, scale: 1.32 },
  { key: "breathHold", seconds: 3, scale: 1.32 },
  { key: "breathOut", seconds: 7, scale: 1.0 },
  { key: "breathHold", seconds: 3, scale: 1.0 }
];

function updateBreathingButtonLabel(dict) {
  const breathBtn = document.getElementById("breathToggle");
  if (!breathBtn) return;
  breathBtn.textContent = breathRunning ? dict.breathStop : dict.breathStart;
}

function renderBreathingState() {
  const bubble = document.getElementById("breathBubble");
  const label = document.getElementById("breathPhase");
  const counter = document.getElementById("breathCount");
  if (!bubble || !label || !counter) return;

  const lang = getLang();
  const dict = translations[lang] || translations.en;

  if (!breathRunning) {
    label.textContent = dict.breathReady;
    counter.textContent = "";
    bubble.style.transform = "scale(1)";
    return;
  }

  const phase = breathPhases[breathPhaseIndex];
  label.textContent = dict[phase.key];
  counter.textContent = `${phase.seconds - breathTick}s`;
  bubble.style.transform = `scale(${phase.scale})`;
}

function startBreathing() {
  if (breathTimer) clearInterval(breathTimer);
  breathRunning = true;
  breathPhaseIndex = 0;
  breathTick = 0;
  renderBreathingState();
  breathTimer = setInterval(() => {
    breathTick += 1;
    const phase = breathPhases[breathPhaseIndex];
    if (breathTick >= phase.seconds) {
      breathTick = 0;
      breathPhaseIndex = (breathPhaseIndex + 1) % breathPhases.length;
    }
    renderBreathingState();
  }, 1000);
}

function stopBreathing() {
  breathRunning = false;
  if (breathTimer) {
    clearInterval(breathTimer);
    breathTimer = null;
  }
  renderBreathingState();
}

function initBreathingWidget() {
  const breathBtn = document.getElementById("breathToggle");
  if (!breathBtn) return;

  breathBtn.addEventListener("click", () => {
    if (breathRunning) stopBreathing();
    else startBreathing();
    const lang = getLang();
    updateBreathingButtonLabel(translations[lang] || translations.en);
  });

  renderBreathingState();
}

