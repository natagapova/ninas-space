function initCalmClick() {
  const meterFill = document.getElementById("meterFill");
  const scoreEl = document.getElementById("calmScore");
  const timeEl = document.getElementById("calmTime");
  const btn = document.getElementById("startCalmClick");
  const setTimeInput = document.getElementById("calmSetTime");
  if (!meterFill || !scoreEl || !timeEl || !btn || !setTimeInput) return;

  let score = 0;
  let time = Number(setTimeInput.value) || 10;
  let timer;
  let running = false;

  function redraw() {
    scoreEl.textContent = score.toString();
    timeEl.textContent = time.toString();
    meterFill.style.width = `${Math.min(score * 5, 100)}%`;
  }

  btn.addEventListener("click", () => {
    if (running) return;
    const chosen = Math.max(2, Math.min(60, Number(setTimeInput.value) || 10));
    setTimeInput.value = chosen.toString();
    running = true;
    score = 0;
    time = chosen;
    redraw();

    timer = setInterval(() => {
      time -= 1;
      redraw();
      if (time <= 0) {
        clearInterval(timer);
        running = false;
      }
    }, 1000);
  });

  meterFill.parentElement.addEventListener("click", () => {
    if (!running) return;
    score += 1;
    redraw();
  });
}

function initKittyMatch() {
  const board = document.getElementById("kittyBoard");
  const movesEl = document.getElementById("kittyMoves");
  const wonEl = document.getElementById("kittyWon");
  const resetBtn = document.getElementById("resetKitty");
  if (!board || !movesEl || !wonEl || !resetBtn) return;

  const icons = ["🐱", "🎀", "💗", "🌸", "🐱", "🎀", "💗", "🌸"];
  let deck = [];
  let opened = [];
  let solved = 0;
  let moves = 0;

  function shuffle(array) {
    for (let i = array.length - 1; i > 0; i -= 1) {
      const j = Math.floor(Math.random() * (i + 1));
      [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
  }

  function drawBoard() {
    board.innerHTML = "";
    deck.forEach((card, idx) => {
      const cell = document.createElement("button");
      cell.className = "kitty-cell";
      cell.type = "button";
      cell.textContent = card.revealed || card.solved ? card.icon : "❀";
      cell.disabled = card.solved;
      cell.addEventListener("click", () => openCard(idx));
      board.appendChild(cell);
    });
    movesEl.textContent = moves.toString();
  }

  function openCard(index) {
    const card = deck[index];
    if (card.revealed || card.solved || opened.length === 2) return;
    card.revealed = true;
    opened.push(index);
    drawBoard();

    if (opened.length === 2) {
      moves += 1;
      const [first, second] = opened;
      if (deck[first].icon === deck[second].icon) {
        deck[first].solved = true;
        deck[second].solved = true;
        solved += 1;
        opened = [];
        drawBoard();
        if (solved === 4) {
          wonEl.hidden = false;
        }
      } else {
        setTimeout(() => {
          deck[first].revealed = false;
          deck[second].revealed = false;
          opened = [];
          drawBoard();
        }, 600);
      }
    }
  }

  function reset() {
    deck = shuffle([...icons]).map((icon) => ({ icon, revealed: false, solved: false }));
    opened = [];
    solved = 0;
    moves = 0;
    wonEl.hidden = true;
    drawBoard();
  }

  resetBtn.addEventListener("click", reset);
  reset();
}

